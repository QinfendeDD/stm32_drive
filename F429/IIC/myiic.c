#include "myiic.h"
#include "delay.h"

void I2C_Configuration(void)
{					     
	GPIO_InitTypeDef GPIO_InitStructure;
	RCC_APB2PeriphClockCmd(	RCC_AHB1Periph_GPIOE, ENABLE );	//使能GPIOB时钟   
	GPIO_InitStructure.GPIO_Pin = GPIO_Pin_6|GPIO_Pin_5;
	GPIO_InitStructure.GPIO_Mode = GPIO_Mode_OUT;   //推挽输出
	GPIO_InitStructure.GPIO_OType = GPIO_OType_PP;//推挽输出
	GPIO_InitStructure.GPIO_Speed = GPIO_Speed_100MHz;
	GPIO_Init(GPIOE, &GPIO_InitStructure);
	GPIO_SetBits(GPIOE,GPIO_Pin_6|GPIO_Pin_5); 	//PB8,PB9 输出高
}
//SDA设置为输入
void SDA_IN(void)
{
    GPIO_InitTypeDef GPIO_InitStructure;
    RCC_APB2PeriphClockCmd(	RCC_AHB1Periph_GPIOE, ENABLE );	
    GPIO_InitStructure.GPIO_Pin = GPIO_Pin_5; 
    GPIO_InitStructure.GPIO_Mode = GPIO_Mode_IN; 
    GPIO_Init(GPIOE, &GPIO_InitStructure);
}

//SDA设置为输出
void SDA_OUT(void)
{ 
	  GPIO_InitTypeDef GPIO_InitStructure;
    RCC_APB2PeriphClockCmd(	RCC_AHB1Periph_GPIOE, ENABLE );	
    GPIO_InitStructure.GPIO_Pin = GPIO_Pin_5; 
    GPIO_InitStructure.GPIO_Mode = GPIO_Mode_OUT;
	  GPIO_InitStructure.GPIO_OType = GPIO_OType_PP;//推挽输出_OD ; 
    GPIO_InitStructure.GPIO_Speed = GPIO_Speed_100MHz;
    GPIO_Init(GPIOE, &GPIO_InitStructure);
}

//产生IIC起始信号
void IIC_Start(void)
{
	SDA_OUT();     //sda线输出
	IIC_SDA=1;	  	  
	IIC_SCL=1;
	delay_us(4);
 	IIC_SDA=0;//START:when CLK is high,DATA change form high to low 
	delay_us(4);
	IIC_SCL=0;//钳住I2C总线，准备发送或接收数据 
}	  
//产生IIC停止信号
void IIC_Stop(void)
{
	SDA_OUT();//sda线输出
	IIC_SCL=0;
	IIC_SDA=0;//STOP:when CLK is high DATA change form low to high
 	delay_us(4);
	IIC_SCL=1; 
	IIC_SDA=1;//发送I2C总线结束信号
	delay_us(4);							   	
}
//等待应答信号到来
//返回值：1，接收应答失败
//        0，接收应答成功
u8 IIC_Wait_Ack(void)
{
	u8 ucErrTime=0;
	SDA_IN();      //SDA设置为输入  
	IIC_SDA=1;delay_us(1);	   
	IIC_SCL=1;delay_us(1);	 
	while(READ_SDA)
	{
		ucErrTime++;
		if(ucErrTime>250)
		{
			IIC_Stop();
			return 1;
		}
	}
	IIC_SCL=0;//时钟输出0 	   
	return 0;  
} 
//产生ACK应答
void IIC_Ack(void)
{
	IIC_SCL=0;
	SDA_OUT();
	IIC_SDA=0;
	delay_us(2);
	IIC_SCL=1;
	delay_us(2);
	IIC_SCL=0;
}
//不产生ACK应答		    
void IIC_NAck(void)
{
	IIC_SCL=0;
	SDA_OUT();
	IIC_SDA=1;
	delay_us(2);
	IIC_SCL=1;
	delay_us(2);
	IIC_SCL=0;
}					 				     
//IIC发送一个字节
//返回从机有无应答
//1，有应答
//0，无应答			  
void IIC_Send_Byte(u8 txd)
{                        
    u8 t;   
	SDA_OUT(); 	    
    IIC_SCL=0;//拉低时钟开始数据传输
    for(t=0;t<8;t++)
    {              
        IIC_SDA=(txd&0x80)>>7;
        txd<<=1; 	  
		delay_us(2);   //对TEA5767这三个延时都是必须的
		IIC_SCL=1;
		delay_us(2); 
		IIC_SCL=0;	
		delay_us(2);
    }	 
} 	    
//读1个字节，ack=1时，发送ACK，ack=0，发送nACK   
u8 IIC_Read_Byte(unsigned char ack)
{
	unsigned char i,receive=0;
	SDA_IN();//SDA设置为输入
    for(i=0;i<8;i++ )
	{
        IIC_SCL=0; 
        delay_us(2);
		IIC_SCL=1;
        receive<<=1;
        if(READ_SDA)receive++;   
		delay_us(1); 
    }					 
    if (!ack)
        IIC_NAck();//发送nACK
    else
        IIC_Ack(); //发送ACK   
    return receive;
}
uint8_t I2CReadBytes(uint8_t Device,uint8_t Resister,uint8_t Data[],uint8_t Counts)
{
	uint8_t i;
	Device<<=1;
	Device&=0xFE;
	IIC_Start();
	IIC_Send_Byte(Device);
	if(IIC_Wait_Ack())
		return 1;
	IIC_Send_Byte(Resister);
	if(IIC_Wait_Ack())
		return 1;
	Device|=0x01;
	IIC_Start();
	IIC_Send_Byte(Device);
	if(IIC_Wait_Ack())
		return 1;
	for(i=0;i<Counts;i++){
		Data[i]=IIC_Read_Byte(1);
		if(i==Counts-1)
			IIC_NAck();
		else
			IIC_Ack();
	}
	IIC_Stop();
	return 0;
}
uint8_t I2CWriteByte(uint8_t Device,uint8_t Resister,uint8_t Data)
{
//	uint8_t i;
	Device<<=1;
	Device&=0xFE;
	IIC_Start();
	IIC_Send_Byte(Device);
	if(IIC_Wait_Ack())
		return 1;
	IIC_Send_Byte(Resister);
	if(IIC_Wait_Ack())
		return 1;
	IIC_Send_Byte(Data);
	if(IIC_Wait_Ack())
		return 1;
	IIC_Stop();
	return 0;
}
uint8_t I2CWriteBytes(uint8_t Device,uint8_t Resister,uint8_t Data[],uint8_t Counts)
{
	uint8_t i;
	Device<<=1;
	Device&=0xFE;
	IIC_Start();
	IIC_Send_Byte(Device);
	if(IIC_Wait_Ack())
		return 1;
	IIC_Send_Byte(Resister);
	if(IIC_Wait_Ack())
		return 1;
	for(i=0;i<Counts;i++){
		IIC_Send_Byte(Data[i]);
		if(IIC_Wait_Ack())
			return 1;
	}
	IIC_Stop();
	return 0;
}
