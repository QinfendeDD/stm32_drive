//初始化IIC
void IIC_Init_6050(void)
{			
  GPIO_InitTypeDef  GPIO_InitStructure;

  RCC_AHB1PeriphClockCmd(RCC_AHB1Periph_GPIOC, ENABLE);//使能GPIOB时钟

  //GPIOB8,B9初始化设置
  GPIO_InitStructure.GPIO_Pin = GPIO_Pin_1 | GPIO_Pin_2;
  GPIO_InitStructure.GPIO_Mode = GPIO_Mode_OUT;//普通输出模式
  GPIO_InitStructure.GPIO_OType = GPIO_OType_PP;//推挽输出
  GPIO_InitStructure.GPIO_Speed = GPIO_Speed_100MHz;//100MHz
  GPIO_InitStructure.GPIO_PuPd = GPIO_PuPd_UP;//上拉
  GPIO_Init(GPIOC, &GPIO_InitStructure);//初始化
}
//SDA设置为输入
void SDA_IN_6050(void)
{
    GPIO_InitTypeDef GPIO_InitStructure;
    RCC_APB2PeriphClockCmd(RCC_AHB1Periph_GPIOC, ENABLE );	
    GPIO_InitStructure.GPIO_Pin = GPIO_Pin_2; 
    GPIO_InitStructure.GPIO_Mode = GPIO_Mode_IN;//_FLOATING; 
//	  GPIO_InitStructure.GPIO_OType = GPIO_OType_FLOATING;// 
    GPIO_Init(GPIOC, &GPIO_InitStructure);
}

//SDA设置为输出
void SDA_OUT_6050(void)
{ 
	  GPIO_InitTypeDef GPIO_InitStructure;
    RCC_APB2PeriphClockCmd(	RCC_AHB1Periph_GPIOC, ENABLE );	
    GPIO_InitStructure.GPIO_Pin = GPIO_Pin_2; 
    GPIO_InitStructure.GPIO_Mode = GPIO_Mode_OUT;//_OD 
	  GPIO_InitStructure.GPIO_OType = GPIO_OType_OD;// 复用开漏输出
    GPIO_InitStructure.GPIO_Speed = GPIO_Speed_100MHz;
    GPIO_Init(GPIOC, &GPIO_InitStructure);
}

//产生IIC起始信号
void IIC_Start_6050(void)
{
	SDA_OUT_6050();     //sda线输出
	IIC_SDA_6050=1;	  	  
	IIC_SCL_6050=1;
	delay_us(4);
 	IIC_SDA_6050=0;//START:when CLK is high,DATA change form high to low 
	delay_us(4);
	IIC_SCL_6050=0;//钳住I2C总线，准备发送或接收数据 
}	  
//产生IIC停止信号
void IIC_Stop_6050(void)
{
	SDA_OUT_6050();//sda线输出
	IIC_SCL_6050=0;
	IIC_SDA_6050=0;//STOP:when CLK is high DATA change form low to high
 	delay_us(4);
	IIC_SCL_6050=1; 
	IIC_SDA_6050=1;//发送I2C总线结束信号
	delay_us(4);							   	
}
//等待应答信号到来
//返回值：1，接收应答失败
//        0，接收应答成功
u8 IIC_Wait_Ack_6050(void)
{
	u8 ucErrTime=0;
	SDA_IN_6050();      //SDA设置为输入  
	IIC_SDA_6050=1;delay_us(1);	   
	IIC_SCL_6050=1;delay_us(1);	 
	while(READ_SDA_6050)
	{
		ucErrTime++;
		if(ucErrTime>250)
		{
			IIC_Stop_6050();
			return 1;
		}
	}
	IIC_SCL_6050=0;//时钟输出0 	   
	return 0;  
} 
//产生ACK应答
void IIC_Ack_6050(void)
{
	IIC_SCL_6050=0;
	SDA_OUT_6050();
	IIC_SDA_6050=0;
	delay_us(2);
	IIC_SCL_6050=1;
	delay_us(2);
	IIC_SCL_6050=0;
}
//不产生ACK应答		    
void IIC_NAck_6050(void)
{
	IIC_SCL_6050=0;
	SDA_OUT_6050();
	IIC_SDA_6050=1;
	delay_us(2);
	IIC_SCL_6050=1;
	delay_us(2);
	IIC_SCL_6050=0;
}					 				     
//IIC发送一个字节
//返回从机有无应答
//1，有应答
//0，无应答			  
void IIC_Send_Byte_6050(u8 txd)
{                        
    u8 t;   
	SDA_OUT_6050(); 	    
    IIC_SCL_6050=0;//拉低时钟开始数据传输
    for(t=0;t<8;t++)
    {              
        IIC_SDA_6050=(txd&0x80)>>7;
        txd<<=1; 	  
		delay_us(2);   //对TEA5767这三个延时都是必须的
		IIC_SCL_6050=1;
		delay_us(2); 
		IIC_SCL_6050=0;	
		delay_us(2);
    }	 
} 	    
//读1个字节，ack=1时，发送ACK，ack=0，发送nACK   
u8 IIC_Read_Byte_6050(unsigned char ack)
{
	unsigned char i,receive=0;
	SDA_IN_6050();//SDA设置为输入
    for(i=0;i<8;i++ )
	{
        IIC_SCL_6050=0; 
        delay_us(2);
		IIC_SCL_6050=1;
        receive<<=1;
        if(READ_SDA_6050)receive++;   
		delay_us(1); 
    }					 
    if (!ack)
        IIC_NAck_6050();//发送nACK
    else
        IIC_Ack_6050(); //发送ACK   
    return receive;
}



























